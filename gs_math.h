#ifndef GS_MATH_H
#define GS_MATH_H


//==============================================================================================
// Include platform abstraction header files.
// ---------------------------------------------------------------------------------------------
#include "gs_platform.h"
//==============================================================================================


//==============================================================================================
// Include standard C library header files.
// ---------------------------------------------------------------------------------------------
#include <math.h>
//==============================================================================================


//==============================================================================================
// Math defines.
// ---------------------------------------------------------------------------------------------
#define GS_PI 3.141592654f
//==============================================================================================


//==============================================================================================
// Function prototypes.
// ----------------------------------------------------------------------------------------------------------------------
float GS_Rad2Deg(float angle);
float GS_Deg2Rad(float angle);
float GS_Sin(float angle);
float GS_Cos(float angle);
float GS_ArcTan(float y, float x);
float GS_Abs(float x);
float GS_Sqrt(float x);
//==============================================================================================


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GS_Rad2Deg():
// ---------------------------------------------------------------------------------------------
// Purpose: Converts radians (angle) to degrees.
// ---------------------------------------------------------------------------------------------
// Returns: Degrees.
//==============================================================================================

inline float GS_Rad2Deg(float angle)
{

    return angle * (180.f / GS_PI);
}


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GS_Deg2Rad():
// ---------------------------------------------------------------------------------------------
// Purpose: Converts degrees (angle) to radians.
// ---------------------------------------------------------------------------------------------
// Returns: Radians.
//==============================================================================================

inline float GS_Deg2Rad(float angle)
{

    return angle * (GS_PI / 180.f);
}


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GS_Sin(), GS_Cos() and GS_ArcTan():
// ---------------------------------------------------------------------------------------------
// Purpose: Trig functions using degrees (angle) and coordinates (x,y).
// ---------------------------------------------------------------------------------------------
// Returns: Result depending on function.
//==============================================================================================

inline float GS_Sin(float angle)
{

    return (float) sin((double) GS_Deg2Rad(angle));
}


////////////////////////////////////////////////////////////////////////////////////////////////


inline float GS_Cos(float angle)
{

    return (float) cos((double) GS_Deg2Rad(angle));
}


////////////////////////////////////////////////////////////////////////////////////////////////


inline float GS_ArcTan(float y, float x)
{

    return GS_Rad2Deg((float) atan2((double) y,(double) x));
}


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GS_Abs():
// ---------------------------------------------------------------------------------------------
// Purpose: To determine tha absolute value of a number (i.e. given a number x the absolute
//          value is -x and given a number -x the absolute value is x).
// ---------------------------------------------------------------------------------------------
// Returns: The absolute value.
//==============================================================================================

inline float GS_Abs(float x)
{

    return x >= 0.f ? x : -x;
}


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GS_Sqrt():
// ---------------------------------------------------------------------------------------------
// Purpose: To determine the square root of a number (x).
// ---------------------------------------------------------------------------------------------
// Returns: The square root.
//==============================================================================================

inline float GS_Sqrt(float x)
{

    return (float) sqrt((double) x);
}


////////////////////////////////////////////////////////////////////////////////////////////////

#endif
