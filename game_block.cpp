//==============================================================================================
// GameBlock::GetData():
// ---------------------------------------------------------------------------------------------
// Purpose: ...
// ---------------------------------------------------------------------------------------------
// Returns: Nothing.
//==============================================================================================

int GameBlock::GetData(int nRow, int nCol) {

   if ((nRow > 4) || (nRow<0) || (nCol>4) || (nCol<0)) {
      return -1;
      }

   return m_Data[nRow][nCol];
}


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GameBlock::SetData():
// ---------------------------------------------------------------------------------------------
// Purpose: ...
// ---------------------------------------------------------------------------------------------
// Returns: ...
//==============================================================================================

void GameBlock::SetData(int nValue) {

   for (int nRow = 0; nRow < 4; nRow++) {
      for (int nCol = 0; nCol < 4; nCol++) {
         m_Data[nRow][nCol] = nValue;
         }
      }
}


////////////////////////////////////////////////////////////////////////////////////////////////


void GameBlock::SetData(GameBlock& TempBlock) {

   for (int nRow = 0; nRow < 4; nRow++) {
      for (int nCol = 0; nCol < 4; nCol++) {
         m_Data[nRow][nCol] = TempBlock.GetData(nRow, nCol);
         }
      }
}


////////////////////////////////////////////////////////////////////////////////////////////////


void GameBlock::SetData(int nVal_0_0, int nVal_0_1, int nVal_0_2, int nVal_0_3,
                        int nVal_1_0, int nVal_1_1, int nVal_1_2, int nVal_1_3,
                        int nVal_2_0, int nVal_2_1, int nVal_2_2, int nVal_2_3,
                        int nVal_3_0, int nVal_3_1, int nVal_3_2, int nVal_3_3) {

   m_Data[0][0]=nVal_0_0; m_Data[0][1]=nVal_0_1; m_Data[0][2]=nVal_0_2; m_Data[0][3]=nVal_0_3;
   m_Data[1][0]=nVal_1_0; m_Data[1][1]=nVal_1_1; m_Data[1][2]=nVal_1_2; m_Data[1][3]=nVal_1_3;
   m_Data[2][0]=nVal_2_0; m_Data[2][1]=nVal_2_1; m_Data[2][2]=nVal_2_2; m_Data[2][3]=nVal_2_3;
   m_Data[3][0]=nVal_3_0; m_Data[3][1]=nVal_3_1; m_Data[3][2]=nVal_3_2; m_Data[3][3]=nVal_3_3;
}



////////////////////////////////////////////////////////////////////////////////////////////////


BOOL GameBlock::SetData(int nRow, int nCol, int nValue) {

   if ((nRow > 4) || (nRow<0) || (nCol>4) || (nCol<0)) {
      return FALSE;
      }

   m_Data[nRow][nCol] = nValue;

   return TRUE;
}


