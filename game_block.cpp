//==============================================================================================
// GameBlock::TurnLeft():
// ---------------------------------------------------------------------------------------------
// Purpose: ...
// ---------------------------------------------------------------------------------------------
// Returns: Nothing.
//==============================================================================================

void GameBlock::TurnLeft() {

   GameBlock TempBlock = *this;

   for (int nRow = 0; nRow < 4; nRow++) {
      for (int nCol = 0; nCol < 4; nCol++) {
         m_Data[nRow][nCol] = TempBlock.GetData(nCol, 3-nRow);
         }
      }
}


////////////////////////////////////////////////////////////////////////////////////////////////


//==============================================================================================
// GameBlock::TurnRight():
// ---------------------------------------------------------------------------------------------
// Purpose: ...
// ---------------------------------------------------------------------------------------------
// Returns: Nothing.
//==============================================================================================

void GameBlock::TurnRight() {

   GameBlock TempBlock = *this;

   for (int nRow = 0; nRow < 4; nRow++) {
      for (int nCol = 0; nCol < 4; nCol++) {
         m_Data[nRow][nCol] = TempBlock.GetData(3-nCol, nRow);
         }
      }
}


